                                    <h2 class="font-weight-semi-bold"><a
                                            href="{{ route('news.reflections-on-18') }}">REFLECTIONS OF A YOUNG
                                            NEUROSURGEON ON THE 18th WORLD FEDERATION OF NEUROSURGICAL SOCIETIES
                                            CONGRESS CAPE TOWN 2023-
                                            A NEUROSURGICAL PILGRIMAGE IN THE QUEST FOR “UBUNTU”
                                        </a></h2>

                                    <div class="post-meta">
                                        <span><i class="far fa-user"></i> By <a href="#">DR. AMINATA YANDEH
                                                SALLAH-TJIRONGO

                                            </a>
                                        </span>
                                    </div>
                                    <p class="text-justify">
                                        In June, my colleagues and I submitted our abstracts for the 18th WFNS congress
                                        which was to take place in Cape Town in year end of 2023, the first one to be
                                        held in Sub-Saharan Africa since the establishment of the WFNS in 1955. We were
                                        also racing towards the final year exams of my training in Neurosurgery in
                                        Harare. We temporarily proceeded to forget all about Cape Town to ponder the
                                        final exam looming ahead. It would be folly to even dare think about WFNS 2023
                                        then!
                                    </p>
                                    <p class="text-justify">
                                        We were excited to learn that our abstracts were accepted as e-posters. In
                                        November, while engaged in exam processes, my consultant once again encouraged
                                        me to attend the congress in Cape Town. I took his words to heart and waited
                                        patiently while keeping focused on the exam before me.
                                    </p>
                                    <p class="text-justify"> I was still alive after the exams; and I drove back to home
                                        to Namibia, via Mosi-O-A-Tunya (Victoria Falls) and its sacred spray, the
                                        Caprivi strip, towards home in Windhoek. I traversed the over 2000 Kilometers in
                                        3 days with 2 days to spare before going on to Cape Town. </p>

                                    <p class="text-justify">
                                        I arrived in Cape town in time for the Pre-Congress courses. I felt strange
                                        about being in another town without familiar work obligations after having spent
                                        all the years of training mostly between theatre, call, clinic, presentations,
                                        and in a confined scheduled environment. Nonetheless, I happily and expectantly
                                        went on to my first course, Neuroendoscopy, flush with attendees from the world
                                        over, and run by world famous experts. It was interesting, challenging and
                                        rewarding and I was thrilled to be able to use an exoscope for the first time.
                                        The following day I went to the functional neurosurgery-Pain workshop, another
                                        amazing experience. The tricks and tips of this course had me in awe. An
                                        unassuming and pleasant elderly gentleman gave a lecture to us, and I was left
                                        shocked to know that this was the great Professor Sindou of meningioma
                                        classification fame in person.
                                    </p>
                                    <p class="text-justify">
                                        In this workshop we experienced neuronavigation and got to perform simulations
                                        of cordotomies among other procedures. I was deeply inspired and excited by the
                                        possibilities.
                                    </p>
                                    <p class="text-justify">
                                        That evening, the opening ceremony was held. It was a smorgasbord of speeches
                                        and entertainment celebrating the 18th WFNS congress being held in South Africa.
                                        It felt like a city within a city, a veritable neurosurgical canton, a home,
                                        away from home.
                                    </p>
                                    <h4 align="center">
                                        The Congress was now open!
                                    </h4>
                                    <p class="text-justify">
                                        My problems began the next day in the form of having too many choices, not
                                        enough time, and an inability to clone myself to attend each desirable event.
                                        Every category of neurosurgical activity was represented; subdivisions of
                                        innovation, diversity, spine, skullbase, paediatrics, global, functional,
                                        oncology, vascular, education and anatomy, interalia. I then realized that I
                                        thereafter had to be strategic and focus only on a few activities.
                                    </p>

                                    <p class="text-justify">
                                        By the next day, we were caught up in the whirlwind of the Neurosurgical world.
                                        This time, making program choices with my new strategy was less angst-inducing.
                                        At lunchtime, I visited my 2 E-posters in the exhibition hall. One was “The
                                        usefulness of diagonal Ventricular Dimension in The Assessment of Blocked
                                        Shunt”, the second one a case report on “A Case of a Spinal Intradural
                                        Intramedullary Arachnoid Cyst”. I was humbled to see my work in that hallowed
                                        space, and I took a moment to thank my teachers in my heart. That evening, I
                                        attended a “Women in Neurosurgery” reception, which was a well-attended and
                                        boisterous networking affair.
                                    </p>
                                    <p class="text-justify">
                                        Thursday, the penultimate day of the congress focused on research, advocacy and
                                        increasingly specialized topics. We pressed on, soaking it all up, with the
                                        knowledge that this was a privilege, especially in “our backyard”. I also got to
                                        appreciate that the running of association congresses of such magnitude is not
                                        easy. Visits to the exhibition hall showed cutting-edge tools, to be tried out.
                                        The bringing in of a neurosurgeon and a neurotech expert who had created a
                                        system bridging spinal cord lesions and together with neuro-rehab techniques,
                                        voila! the patients were able to walk again was spectacular. I had been
                                        following their progress and now here I was watching their presentation with
                                        awe, thanking the WFNS all the while.
                                    </p>
                                    <p class="text-justify">
                                        I was fortunate during this congress to attend a meeting of the new WFNS Journal
                                        and met the editor in Chief, a luminary neurosurgeon, Professor Anil Nanda, he
                                        encouraged attendees to spread the word about it and submit articles.
                                        Finally, we were informed of the next Continental African Association of
                                        Neurosurgical Societies (CAANS) meeting in 2024 in Kinshasa and Brazzaville, and
                                        the next WFNS Congress in 2025 in Madrid, Spain. Although it was so sad to
                                        disperse, we could look forward to the next meetings with my new tribe, the
                                        Neurosurgical tribe.

                                    </p>
                                   <p class="text-justify">
                                    I left Cape Town to return to Namibia, off to quiet homesteading and relaxation in our village, thankful and satisfied, after so many overwhelming, inspiring, and professionally energizing experiences. Memories that will endure.
                                   </p>
